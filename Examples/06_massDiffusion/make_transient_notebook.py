# ── Cell 1: problem description ───────────────────────────────────────────────
cells.append(md([
    "## Transient Mass Diffusion\n",
    "\n",
    "We consider transient mass diffusion in a 2-D heterogeneous material:\n",
    "$$\n",
    "\\frac{\\partial C}{\\partial t} = \\nabla\\cdot\\bigl(D(x,y)\\,\\nabla C\\bigr),\n",
    "\\quad (x,y)\\in[0,1]^2,\\; t\\in[0,T]\n",
    "$$\n",
    "\n",
    "**Boundary conditions**\n",
    "- Left ($x=0$): $C=1$ &nbsp; (Dirichlet)\n",
    "- Right ($x=1$): $C=0$ &nbsp; (Dirichlet)\n",
    "- Top/bottom: zero normal flux (Neumann)\n",
    "\n",
    "**Initial condition**: $C(x,y,0)=1-x$\n",
    "&nbsp;&nbsp;*(linear profile — consistent with the BCs, avoids corner singularities)*\n",
    "\n",
    "**Diffusivity**: $D(x,y)=\\exp(\\alpha x+\\beta y)$, $\\;\\alpha,\\beta\\sim\\mathrm{Uniform}(-1,1)$\n",
    "\n",
    "### Neural operator learning problem\n",
    "$$\\mathcal{G}: D(x,y) \\;\\longrightarrow\\; C(x,y,t)$$\n",
    "\n",
    "The trunk net takes $(x,y,t)$ as input.  \n",
    "The time-gated mollifier encodes **all** constraints by construction:\n",
    "$$C = u_\\theta\\cdot x(1-x)\\sin(\\pi y)\\cdot\\underbrace{(1-e^{-t/\\tau})}_{\\text{time gate}} + (1-x)$$\n",
    "- $t=0$: time gate $=0 \\Rightarrow C=1-x$ &nbsp;✓ (IC exactly)\n",
    "- $x=0$: $C=1$ &nbsp;✓ &nbsp; $x=1$: $C=0$ &nbsp;✓\n",
    "- $y=0,1$: $C=1-x$ (Neumann-consistent) &nbsp;✓\n",
]))

