# ── Cell 3: method description ────────────────────────────────────────────────
cells.append(md([
    "## PIDeepONet — transient formulation\n",
    "\n",
    "### PDE loss\n",
    "At each training step, $N_t=5$ time points are sampled uniformly in $(0,T]$.\n",
    "For each $t_k$:\n",
    "1. Evaluate $C$ on the $29\\times29$ spatial mesh via the model + mollifier.\n",
    "2. Compute $\\partial C/\\partial t$ by **automatic differentiation** through the trunk net.\n",
    "3. Compute $\\nabla\\cdot(D\\nabla C)$ by two passes of the same FDM used in the steady-state case.\n",
    "4. Accumulate residual $\\|\\partial C/\\partial t - \\nabla\\cdot(D\\nabla C)\\|^2$.\n",
    "\n",
    "No IC loss is needed — the time gate in the mollifier satisfies $C(x,y,0)=1-x$ exactly.\n",
]))

# ── Cell 4: LossClass + model ─────────────────────────────────────────────────
cells.append(code([
    "from Utils.Grad import *\n",
    "import torch.nn as nn\n",
    "from torch.autograd import Variable\n",
    "\n",
    "N_t_pde = 5   # time points sampled per batch for PDE residual\n",
    "\n",
    "# ── nearest-neighbour lookup of D on the spatial mesh ────────────────────────\n",
    "class fun_a(object):\n",
    "    def __init__(self, res):\n",
    "        self.res   = res\n",
    "        self.delta = 1. / (res - 1)\n",
    "\n",
    "    def __call__(self, x, a):\n",
    "        # x: (N, 2) spatial-only points\n",
    "        a = a.squeeze(-1)\n",
    "        x_loc = torch.floor(x[..., 0] / self.delta + 0.5).int()\n",
    "        y_loc = torch.floor(x[..., 1] / self.delta + 0.5).int()\n",
    "        loc   = y_loc * self.res + x_loc\n",
    "        img   = a[torch.arange(a.size(0)).unsqueeze(1), loc]\n",
    "        return img.unsqueeze(-1)\n",
    "\n",
    "\n",
    "# ── time-gated mollifier ──────────────────────────────────────────────────────\n",
    "class mollifier(object):\n",
    "    # C = u_nn * x*(1-x)*sin(pi*y) * (1 - exp(-t/tau))  +  (1-x)\n",
    "    # Satisfies IC (t=0), Dirichlet BCs (x=0,1), and Neumann-consistency (y=0,1)\n",
    "    def __call__(self, u, x):\n",
    "        xx = x[..., 0:1]\n",
    "        yy = x[..., 1:2]\n",
    "        tt = x[..., 2:3]\n",
    "        time_gate = 1.0 - torch.exp(-tt / tau)\n",
    "        return u * xx * (1 - xx) * torch.sin(np.pi * yy) * time_gate + (1 - xx)\n",
    "\n",
    "\n",
    "# ── LossClass ─────────────────────────────────────────────────────────────────\n",
    "class LossClass(object):\n",
    "\n",
    "    def __init__(self, solver):\n",
    "        self.solver    = solver\n",
    "        self.dtype     = solver.dtype\n",
    "        self.device    = solver.device\n",
    "        self.fun_a     = fun_a(res=res)\n",
    "        self.model_u   = solver.model_dict['u']\n",
    "        self.mollifier = mollifier()\n",
    "        self.deltax    = 1. / (N_mesh - 1)\n",
    "        self.deltay    = 1. / (N_mesh - 1)\n",
    "\n",
    "    def Loss_pde(self, a_batch, w_pde):\n",
    "        '''PDE loss: dC/dt - div(D grad C) = 0'''\n",
    "        n_batch = a_batch.shape[0]\n",
    "        if w_pde == 0.:\n",
    "            return torch.tensor(0.)\n",
    "\n",
    "        # D values at spatial mesh (time-independent)\n",
    "        a_sp   = self.fun_a(x_mesh, a_batch)                       # (batch, N², 1)\n",
    "        a_grid = a_sp.reshape(n_batch, N_mesh, N_mesh, 1)\n",
    "\n",
    "        # sample time points uniformly in (0, T]\n",
    "        t_pts = torch.linspace(T / N_t_pde, T, N_t_pde, dtype=self.dtype)\n",
    "\n",
    "        loss_total = torch.tensor(0., device=self.device)\n",
    "\n",
    "        for t_k in t_pts:\n",
    "            # build (x, y, t_k) input with requires_grad so autograd flows through t\n",
    "            t_col = torch.full((N_mesh * N_mesh, 1), t_k.item(), dtype=self.dtype)\n",
    "            x_st  = torch.cat([x_mesh, t_col], dim=-1)             # (N², 3)\n",
    "            x_st  = Variable(\n",
    "                x_st.unsqueeze(0).repeat(n_batch, 1, 1).to(self.device),\n",
    "                requires_grad=True)\n",
    "\n",
    "            u = self.model_u(x_st, a_batch)\n",
    "            u = self.mollifier(u, x_st)                            # (batch, N², 1)\n",
    "\n",
    "            # dC/dt via autograd w.r.t. x_st (last column = t)\n",
    "            grads  = torch.autograd.grad(u.sum(), x_st, create_graph=True)[0]\n",
    "            du_dt  = grads[..., 2:3]                               # (batch, N², 1)\n",
    "\n",
    "            # spatial divergence via FDM (two passes, each trims 1 layer/side)\n",
    "            u_grid = u.reshape(n_batch, N_mesh, N_mesh, 1)\n",
    "            dudx, dudy = FDM_2d(u_grid, self.deltax, self.deltay)  # → (batch, N-2, N-2, 1)\n",
    "            adux = a_grid[:, 1:-1, 1:-1, :] * dudx\n",
    "            aduy = a_grid[:, 1:-1, 1:-1, :] * dudy\n",
    "            dauxdx, _      = FDM_2d(adux, self.deltax, self.deltay)  # → (batch, N-4, N-4, 1)\n",
    "            _,      dauydy = FDM_2d(aduy, self.deltax, self.deltay)\n",
    "\n",
    "            # trim du_dt to the same (N-4)² interior after two FDM passes\n",
    "            du_dt_int = du_dt.reshape(n_batch, N_mesh, N_mesh, 1)[:, 2:-2, 2:-2, :]\n",
    "\n",
    "            residual = (du_dt_int - (dauxdx + dauydy)).reshape(n_batch, -1)\n",
    "            loss_total += self.solver.getLoss(residual, torch.zeros_like(residual))\n",
    "\n",
    "        return loss_total / N_t_pde\n",
    "\n",
    "    def Loss_data(self, x, a, u, w_data):\n",
    "        return torch.tensor(0.)\n",
    "\n",
    "    def Error(self, x, a, u):\n",
    "        u_pred = self.model_u(x, a)\n",
    "        u_pred = self.mollifier(u_pred, x)\n",
    "        return self.solver.getError(u_pred, u)\n",
    "\n",
    "\n",
    "# ── model architecture ────────────────────────────────────────────────────────\n",
    "from Solvers.PIDeepONet import PIDeepONet\n",
    "from Networks.CNNet import CNNet2d\n",
    "\n",
    "solver  = PIDeepONet.Solver(device=device, dtype=dtype)\n",
    "netType = 'DeepONetBatch'\n",
    "\n",
    "class BranchNet(nn.Module):\n",
    "    def __init__(self, conv_arch, fc_arch, nx_size, ny_size, dtype=None):\n",
    "        super().__init__()\n",
    "        self.nx_size, self.ny_size = nx_size, ny_size\n",
    "        self.conv = CNNet2d(conv_arch=conv_arch, fc_arch=fc_arch,\n",
    "                            activation_conv='SiLU', activation_fc='SiLU',\n",
    "                            kernel_size=(3, 3), stride=2, dtype=dtype)\n",
    "    def forward(self, x):\n",
    "        x = x.reshape(-1, self.ny_size, self.nx_size).unsqueeze(1)\n",
    "        return self.conv(x)\n",
    "\n",
    "conv_arch = [1, 64, 64, 64]\n",
    "fc_arch   = [64 * 2 * 2, 128, 128, 128]\n",
    "branchNet = BranchNet(conv_arch, fc_arch, nx_size=res, ny_size=res, dtype=dtype)\n",
    "\n",
    "# trunk net: 3 inputs (x, y, t) instead of 2\n",
    "layers_branch, activation_branch = [branchNet, fc_arch[-1]], 'SiLU'\n",
    "layers_trunk,  activation_trunk  = [3, 128, 128, 128, 128], 'ReLU'\n",
    "\n",
    "model_u = solver.getModel(layers_branch, layers_trunk, activation_branch, activation_trunk,\n",
    "                           multi_ouput_strategy=None, num_output=1, netType=netType)\n",
    "\n",
    "total = sum(p.numel() for p in model_u.parameters() if p.requires_grad)\n",
    "print(f'{total:,} trainable parameters.')\n",
]))

